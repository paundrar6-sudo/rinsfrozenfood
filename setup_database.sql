-- 1. Buat tabel produk
create table if not exists products (
  id bigint primary key,
  nama text not null default 'Nama Produk Baru',
  kategori text not null default 'lainnya',
  harga numeric not null default 0,
  deskripsi text default 'Deskripsi produk di sini.',
  emoji text default '📦',
  gambar_url text default '',
  unggulan boolean default false,
  stok integer default 0,
  aktif boolean default true,
  updated_at timestamptz default now()
);

-- 2. Aktifkan Row Level Security
alter table products enable row level security;

-- 3. Izinkan SIAPA SAJA membaca data produk (untuk web utama / publik)
drop policy if exists "Public read access" on products;
create policy "Public read access" on products
  for select using (true);

-- 4. Izinkan SIAPA SAJA mengubah data (untuk admin panel sederhana tanpa login)
--    CATATAN KEAMANAN: ini cocok untuk tahap awal / internal saja.
--    Untuk produksi yang lebih aman, sebaiknya tambahkan Supabase Auth nanti.
drop policy if exists "Public write access" on products;
create policy "Public write access" on products
  for all using (true) with check (true);

